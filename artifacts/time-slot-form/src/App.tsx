import { Switch, Route, Router as WouterRouter } from "wouter";
import { QueryClient, QueryClientProvider } from "@tanstack/react-query";
import { Toaster } from "@/components/ui/toaster";
import { TooltipProvider } from "@/components/ui/tooltip";
import Home from "@/pages/Home";
import Teacher from "@/pages/Teacher";
import TeacherGate from "@/pages/TeacherGate";
import TeacherDirectory from "@/pages/TeacherDirectory";
import TeacherSignup from "@/pages/TeacherSignup";
import NotFound from "@/pages/not-found";

const queryClient = new QueryClient({
  defaultOptions: {
    queries: {
      refetchOnWindowFocus: false,
      staleTime: 1000 * 60 * 5,
    },
  },
});

function Router() {
  return (
    <Switch>
      <Route path="/" component={TeacherDirectory} />
      <Route path="/book/:slug" component={Home} />
      <Route path="/teacher/signup" component={TeacherSignup} />
      <Route path="/teacher">
        <TeacherGate>
          <Teacher />
        </TeacherGate>
      </Route>
      <Route component={NotFound} />
    </Switch>
  );
}

function App() {
  return (
    <QueryClientProvider client={queryClient}>
      <TooltipProvider>
        <WouterRouter base={import.meta.env.BASE_URL.replace(/\/$/, "")}>
          <Router />
        </WouterRouter>
        <Toaster />
      </TooltipProvider>
    </QueryClientProvider>
  );
}

export default App;
